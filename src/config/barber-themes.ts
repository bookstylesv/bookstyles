/**
 * Sistema de temas visual para BarberPro.
 * Usa variables CSS HSL (formato "h s% l%") para máxima flexibilidad.
 * Se aplican con document.documentElement.style.setProperty()
 *
 * Categorías:
 *  - "barberia" : Barberías clásicas y modernas
 *  - "salon"    : Salones de belleza, spa, estilistas
 */

export interface BarberTheme {
  id: string;
  name: string;
  emoji: string;
  description: string;
  category: "barberia" | "salon";
  isDark: boolean;
  colorPrimary: string; // hex para Ant Design ConfigProvider
  preview: [string, string, string]; // [sidebar, primary, content]
  vars: Record<string, string>; // sobreescrituras de CSS variables
}

// ─── Helper: aplica el tema al :root ────────────────────────────────────────
export function applyBarberTheme(theme: BarberTheme): void {
  const root = document.documentElement;
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value);
  }
  root.setAttribute("data-theme", theme.id);
}

export function getThemeById(id: string): BarberTheme {
  return BARBER_THEMES.find((t) => t.id === id) ?? BARBER_THEMES[0];
}

